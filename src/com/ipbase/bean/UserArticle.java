package com.ipbase.bean;

import cn.bmob.v3.BmobObject;
import cn.bmob.v3.datatype.BmobFile;

@ SuppressWarnings( "serial" )
public class UserArticle extends BmobObject
{
	private String id;
	private String phone;
	private String password;
	private String nickname;
	private String email;
	private String weixin;
	private String aipay;
	private BmobFile mWord;
	private int state;

	public BmobFile getWord()
	{
		return mWord;
	}

	public void setWord( BmobFile mWord )
	{
		this.mWord = mWord;
	}

	public String getId()
	{
		return id;
	}

	public String getPhone()
	{
		return phone;
	}

	public String getPassword()
	{
		return password;
	}

	public String getNickname()
	{
		return nickname;
	}

	public String getEmail()
	{
		return email;
	}

	public String getWeixin()
	{
		return weixin;
	}

	public String getAipay()
	{
		return aipay;
	}

	public int getState()
	{
		return state;
	}

	public void setId( String id )
	{
		this.id = id;
	}

	public void setPhone( String phone )
	{
		this.phone = phone;
	}

	public void setPassword( String password )
	{
		this.password = password;
	}

	public void setNickname( String nickname )
	{
		this.nickname = nickname;
	}

	public void setEmail( String email )
	{
		this.email = email;
	}

	public void setWeixin( String weixin )
	{
		this.weixin = weixin;
	}

	public void setAipay( String aipay )
	{
		this.aipay = aipay;
	}

	public void setState( int state )
	{
		this.state = state;
	}

	@ Override
	public String toString()
	{
		return "User [id=" + id + ", phone=" + phone + ", password=" + password
				+ ", nickname=" + nickname + ", email=" + email + ", weixin="
				+ weixin + ", aipay=" + aipay + ", state=" + state + "]";
	}
}
